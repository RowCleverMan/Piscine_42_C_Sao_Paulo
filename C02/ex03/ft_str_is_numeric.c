int		ft_str_is_numeric(char *str)
{
	int i;
	int is_num;

	is_num = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			is_num = 1;
		}
		else
			return (0);
		i++;
	}
	return (is_num);
}
