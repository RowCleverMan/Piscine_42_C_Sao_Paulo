#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char alpha;

	alpha = 97;
	while (alpha <= 122)
	{
		ft_putchar(alpha);
		alpha++;
	}
}
